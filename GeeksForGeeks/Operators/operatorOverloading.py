# Using + operator to add two integers
print(10 + 20)

# Using + operator to concatenate two strings
print("Sumedh " + "W. " + "Jambekar")

# Using + operator to concatenate two lists
ListOne = ["Sumedh ", "W. ", "Jambekar"]
ListTwo = ["Kalaburagi", "Karnataka", "India"]
print(ListOne + ListTwo)

# Using + operator to concatenate two tuples
TupleOne = ("Sumedh ")
TupleTwo = ("Jambekar")
print(TupleOne + TupleTwo)

