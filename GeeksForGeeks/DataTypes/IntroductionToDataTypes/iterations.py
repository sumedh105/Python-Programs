# Iteration by while loop
i = 1
while i < 10:
    print(i)
    i += 1

# Iteration by for loop on string
string = "Sumedh W. Jambekar from Karnataka, India"
for index in string:
    print(index)

# Iteration by for loop on list
List = ["Sumedh W. Jambekar", "Kalaburagi", "Karnataka", "India"]
for index in List:
    print(index)

# Iteration by for loop for range
for index in range(0, 10):
    print(index)
