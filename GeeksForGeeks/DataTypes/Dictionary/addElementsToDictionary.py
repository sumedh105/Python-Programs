dictOne = {}
print(dictOne)

# Adding elements one at a time to dictionary
dictOne[0] = "Sumedh"
dictOne[1] = "W."
dictOne[2] = "Jambekar"
print(dictOne)

# Adding a value set to a dictionary
dictOne["ValueSet"] = "Kalaburagi", "Karnataka", "India"
print(dictOne)

# Updating the existing value in a dictionary
dictOne[1] = "WB"
print(dictOne)
