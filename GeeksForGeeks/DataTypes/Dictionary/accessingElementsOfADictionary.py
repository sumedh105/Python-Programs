# Creating a dictionary
dictOne = {"Name":"Sumedh W. Jambekar", "DoB":"16-03-1987", "Country":"India"}
print(dictOne)

# Accessing elements of a dictionary
print(dictOne["Name"])
print(dictOne["DoB"])
print(dictOne["Country"])

# Using get() to access elements from a dictionary
print(dictOne.get("Name"))
print(dictOne.get("DoB"))
print(dictOne.get("Country"))
